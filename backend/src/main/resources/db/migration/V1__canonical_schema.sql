    CONSTRAINT fk_ventas_stock_stock FOREIGN KEY (stock_id) REFERENCES public.stocks(id) ON DELETE RESTRICT
);

CREATE INDEX ix_ventas_stock_alumno_fecha ON public.ventas_stock (alumno_id, fecha);
CREATE INDEX ix_ventas_stock_stock ON public.ventas_stock (stock_id);

CREATE TABLE public.cargos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    alumno_id BIGINT NOT NULL,
    tipo VARCHAR(20) NOT NULL,
    descripcion VARCHAR(255) NOT NULL,
    importe_original NUMERIC(19,2) NOT NULL,
    fecha_emision DATE NOT NULL,
    fecha_vencimiento DATE NOT NULL,
    estado VARCHAR(10) NOT NULL DEFAULT 'PENDIENTE',
    mensualidad_id BIGINT,
    matricula_id BIGINT,
    concepto_id BIGINT,
    venta_stock_id BIGINT,
    cargo_origen_id BIGINT,
    idempotency_key VARCHAR(100),
    version BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_cargos_importe CHECK (importe_original >= 0),
    CONSTRAINT ck_cargos_tipo CHECK (tipo IN ('MENSUALIDAD','MATRICULA','CONCEPTO','VENTA_STOCK','RECARGO')),
    CONSTRAINT ck_cargos_estado CHECK (estado IN ('PENDIENTE','PARCIAL','PAGADO','ANULADO')),
    CONSTRAINT ck_cargos_origen CHECK (
        (tipo = 'MENSUALIDAD' AND mensualidad_id IS NOT NULL AND matricula_id IS NULL AND concepto_id IS NULL AND venta_stock_id IS NULL AND cargo_origen_id IS NULL) OR
        (tipo = 'MATRICULA' AND mensualidad_id IS NULL AND matricula_id IS NOT NULL AND concepto_id IS NULL AND venta_stock_id IS NULL AND cargo_origen_id IS NULL) OR
        (tipo = 'CONCEPTO' AND mensualidad_id IS NULL AND matricula_id IS NULL AND concepto_id IS NOT NULL AND venta_stock_id IS NULL AND cargo_origen_id IS NULL) OR
        (tipo = 'VENTA_STOCK' AND mensualidad_id IS NULL AND matricula_id IS NULL AND concepto_id IS NULL AND venta_stock_id IS NOT NULL AND cargo_origen_id IS NULL) OR
        (tipo = 'RECARGO' AND mensualidad_id IS NULL AND matricula_id IS NULL AND concepto_id IS NULL AND venta_stock_id IS NULL AND cargo_origen_id IS NOT NULL)
    ),
    CONSTRAINT uq_cargos_mensualidad UNIQUE (mensualidad_id),
    CONSTRAINT uq_cargos_matricula UNIQUE (matricula_id),
    CONSTRAINT uq_cargos_venta_stock UNIQUE (venta_stock_id),
    CONSTRAINT uq_cargos_idempotency UNIQUE (idempotency_key),
    CONSTRAINT fk_cargos_alumno FOREIGN KEY (alumno_id) REFERENCES public.alumnos(id) ON DELETE RESTRICT,
    CONSTRAINT fk_cargos_mensualidad FOREIGN KEY (mensualidad_id) REFERENCES public.mensualidades(id) ON DELETE RESTRICT,
    CONSTRAINT fk_cargos_matricula FOREIGN KEY (matricula_id) REFERENCES public.matriculas(id) ON DELETE RESTRICT,
    CONSTRAINT fk_cargos_concepto FOREIGN KEY (concepto_id) REFERENCES public.conceptos(id) ON DELETE RESTRICT,
    CONSTRAINT fk_cargos_venta_stock FOREIGN KEY (venta_stock_id) REFERENCES public.ventas_stock(id) ON DELETE RESTRICT
    ,CONSTRAINT fk_cargos_cargo_origen FOREIGN KEY (cargo_origen_id) REFERENCES public.cargos(id) ON DELETE RESTRICT
);

CREATE INDEX ix_cargos_alumno_estado_vencimiento
    ON public.cargos (alumno_id, estado, fecha_vencimiento);
CREATE INDEX ix_cargos_pendientes
    ON public.cargos (fecha_vencimiento, alumno_id) WHERE estado IN ('PENDIENTE','PARCIAL');
CREATE INDEX ix_cargos_concepto ON public.cargos (concepto_id);
CREATE INDEX ix_cargos_cargo_origen ON public.cargos (cargo_origen_id);

CREATE TABLE public.pagos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    alumno_id BIGINT NOT NULL,
    metodo_pago_id BIGINT NOT NULL,
    usuario_id BIGINT NOT NULL,
    fecha DATE NOT NULL,
    monto_recibido NUMERIC(19,2) NOT NULL,
    estado VARCHAR(10) NOT NULL DEFAULT 'REGISTRADO',
    idempotency_key VARCHAR(100) NOT NULL,
    request_hash VARCHAR(64) NOT NULL,
    reversal_idempotency_key VARCHAR(100),
    observaciones VARCHAR(500),
    motivo_anulacion VARCHAR(500),
    fecha_anulacion TIMESTAMPTZ,
    version BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_pagos_monto CHECK (monto_recibido > 0),
    CONSTRAINT ck_pagos_estado CHECK (estado IN ('REGISTRADO','ANULADO')),
    CONSTRAINT ck_pagos_anulacion CHECK ((estado = 'REGISTRADO' AND fecha_anulacion IS NULL AND motivo_anulacion IS NULL) OR (estado = 'ANULADO' AND fecha_anulacion IS NOT NULL AND motivo_anulacion IS NOT NULL)),
    CONSTRAINT uq_pagos_idempotency UNIQUE (idempotency_key),
    CONSTRAINT uq_pagos_reversal_idempotency UNIQUE (reversal_idempotency_key),
    CONSTRAINT fk_pagos_alumno FOREIGN KEY (alumno_id) REFERENCES public.alumnos(id) ON DELETE RESTRICT,
    CONSTRAINT fk_pagos_metodo FOREIGN KEY (metodo_pago_id) REFERENCES public.metodo_pagos(id) ON DELETE RESTRICT,
    CONSTRAINT fk_pagos_usuario FOREIGN KEY (usuario_id) REFERENCES public.usuarios(id) ON DELETE RESTRICT
);

CREATE INDEX ix_pagos_alumno_fecha ON public.pagos (alumno_id, fecha DESC);
CREATE INDEX ix_pagos_metodo ON public.pagos (metodo_pago_id);
CREATE INDEX ix_pagos_usuario ON public.pagos (usuario_id);

