CREATE TABLE public.roles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descripcion VARCHAR(50) NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_roles_descripcion UNIQUE (descripcion)
);

CREATE TABLE public.usuarios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_usuario VARCHAR(100) NOT NULL,
    contrasena VARCHAR(100) NOT NULL,
    rol_id BIGINT NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_usuarios_rol FOREIGN KEY (rol_id) REFERENCES public.roles(id) ON DELETE RESTRICT
);

CREATE UNIQUE INDEX uq_usuarios_nombre_normalizado
    ON public.usuarios (lower(nombre_usuario));
CREATE INDEX ix_usuarios_rol ON public.usuarios (rol_id);

CREATE TABLE public.alumnos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    apellido VARCHAR(100),
    fecha_nacimiento DATE,
    celular1 VARCHAR(30),
    celular2 VARCHAR(30),
    email VARCHAR(254),
    documento VARCHAR(30),
    cuit VARCHAR(20),
    fecha_incorporacion DATE NOT NULL,
    fecha_de_baja DATE,
    nombre_padres VARCHAR(200),
    autorizado_para_salir_solo BOOLEAN,
    otras_notas TEXT,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    version BIGINT NOT NULL DEFAULT 0,
    CONSTRAINT ck_alumnos_baja CHECK ((activo AND fecha_de_baja IS NULL) OR NOT activo)
);

CREATE UNIQUE INDEX uq_alumnos_documento
    ON public.alumnos (documento) WHERE documento IS NOT NULL;
CREATE INDEX ix_alumnos_activos_nombre
    ON public.alumnos (activo, apellido, nombre);

CREATE TABLE public.salones (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    descripcion VARCHAR(255),
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_salones_nombre UNIQUE (nombre)
);

CREATE TABLE public.profesores (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    apellido VARCHAR(100) NOT NULL,
    fecha_nacimiento DATE,
    telefono VARCHAR(30),
    usuario_id BIGINT,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    version BIGINT NOT NULL DEFAULT 0,
    CONSTRAINT uq_profesores_usuario UNIQUE (usuario_id),
    CONSTRAINT fk_profesores_usuario FOREIGN KEY (usuario_id) REFERENCES public.usuarios(id) ON DELETE RESTRICT
);

CREATE TABLE public.observaciones_profesores (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profesor_id BIGINT NOT NULL,
    fecha DATE NOT NULL,
    observacion TEXT NOT NULL,
    activa BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_observaciones_profesor FOREIGN KEY (profesor_id) REFERENCES public.profesores(id) ON DELETE RESTRICT
);

CREATE INDEX ix_observaciones_profesor_fecha
    ON public.observaciones_profesores (profesor_id, fecha);

CREATE TABLE public.bonificaciones (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descripcion VARCHAR(150) NOT NULL,
    porcentaje_descuento NUMERIC(7,4) NOT NULL DEFAULT 0,
    valor_fijo NUMERIC(19,2) NOT NULL DEFAULT 0,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    observaciones VARCHAR(500),
    CONSTRAINT uq_bonificaciones_descripcion UNIQUE (descripcion),
    CONSTRAINT ck_bonificaciones_porcentaje CHECK (porcentaje_descuento BETWEEN 0 AND 100),
    CONSTRAINT ck_bonificaciones_valor CHECK (valor_fijo >= 0)
);

CREATE TABLE public.recargos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descripcion VARCHAR(150) NOT NULL,
    porcentaje NUMERIC(7,4) NOT NULL DEFAULT 0,
    valor_fijo NUMERIC(19,2) NOT NULL DEFAULT 0,
    dia_del_mes_aplicacion INTEGER,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_recargos_descripcion UNIQUE (descripcion),
    CONSTRAINT ck_recargos_porcentaje CHECK (porcentaje BETWEEN 0 AND 100),
    CONSTRAINT ck_recargos_valor CHECK (valor_fijo >= 0),
    CONSTRAINT ck_recargos_dia CHECK (dia_del_mes_aplicacion IS NULL OR dia_del_mes_aplicacion BETWEEN 1 AND 31)
);

CREATE TABLE public.metodo_pagos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descripcion VARCHAR(100) NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    recargo NUMERIC(7,4) NOT NULL DEFAULT 0,
    CONSTRAINT uq_metodos_pago_descripcion UNIQUE (descripcion),
    CONSTRAINT ck_metodos_pago_recargo CHECK (recargo BETWEEN 0 AND 100)
);

CREATE TABLE public.sub_conceptos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descripcion VARCHAR(150) NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_sub_conceptos_descripcion UNIQUE (descripcion)
);

CREATE TABLE public.conceptos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descripcion VARCHAR(150) NOT NULL,
    precio NUMERIC(19,2) NOT NULL,
    sub_concepto_id BIGINT NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT ck_conceptos_precio CHECK (precio >= 0),
    CONSTRAINT uq_conceptos_sub_descripcion UNIQUE (sub_concepto_id, descripcion),
    CONSTRAINT fk_conceptos_sub_concepto FOREIGN KEY (sub_concepto_id) REFERENCES public.sub_conceptos(id) ON DELETE RESTRICT
);

CREATE INDEX ix_conceptos_sub_concepto ON public.conceptos (sub_concepto_id);

CREATE TABLE public.stocks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(150) NOT NULL,
    precio NUMERIC(19,2) NOT NULL,
    cantidad_actual INTEGER NOT NULL DEFAULT 0,
    requiere_control_de_stock BOOLEAN NOT NULL DEFAULT TRUE,
    codigo_barras VARCHAR(100),
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    version BIGINT NOT NULL DEFAULT 0,
    CONSTRAINT uq_stocks_nombre UNIQUE (nombre),
    CONSTRAINT ck_stocks_precio CHECK (precio >= 0),
    CONSTRAINT ck_stocks_cantidad CHECK (cantidad_actual >= 0)
);

CREATE UNIQUE INDEX uq_stocks_codigo_barras
    ON public.stocks (codigo_barras) WHERE codigo_barras IS NOT NULL;
CREATE INDEX ix_stocks_activos_nombre ON public.stocks (activo, nombre);

CREATE TABLE public.disciplinas (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(150) NOT NULL,
    salon_id BIGINT,
    profesor_id BIGINT NOT NULL,
    valor_cuota NUMERIC(19,2) NOT NULL,
    matricula NUMERIC(19,2) NOT NULL DEFAULT 0,
    clase_suelta NUMERIC(19,2) NOT NULL DEFAULT 0,
    clase_prueba NUMERIC(19,2) NOT NULL DEFAULT 0,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    version BIGINT NOT NULL DEFAULT 0,
    CONSTRAINT ck_disciplinas_valor_cuota CHECK (valor_cuota >= 0),
    CONSTRAINT ck_disciplinas_matricula CHECK (matricula >= 0),
    CONSTRAINT ck_disciplinas_clase_suelta CHECK (clase_suelta >= 0),
    CONSTRAINT ck_disciplinas_clase_prueba CHECK (clase_prueba >= 0),
    CONSTRAINT fk_disciplinas_salon FOREIGN KEY (salon_id) REFERENCES public.salones(id) ON DELETE RESTRICT,
    CONSTRAINT fk_disciplinas_profesor FOREIGN KEY (profesor_id) REFERENCES public.profesores(id) ON DELETE RESTRICT
);

CREATE INDEX ix_disciplinas_salon ON public.disciplinas (salon_id);
CREATE INDEX ix_disciplinas_profesor ON public.disciplinas (profesor_id);
CREATE INDEX ix_disciplinas_activo_nombre ON public.disciplinas (activo, nombre);

CREATE TABLE public.disciplina_horarios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    disciplina_id BIGINT NOT NULL,
    dia_semana VARCHAR(12) NOT NULL,
    horario_inicio TIME NOT NULL,
    duracion NUMERIC(5,2) NOT NULL,
    CONSTRAINT ck_horarios_dia CHECK (dia_semana IN ('LUNES','MARTES','MIERCOLES','JUEVES','VIERNES','SABADO','DOMINGO')),
    CONSTRAINT ck_horarios_duracion CHECK (duracion > 0),
    CONSTRAINT uq_horarios_disciplina UNIQUE (disciplina_id, dia_semana, horario_inicio),
    CONSTRAINT fk_horarios_disciplina FOREIGN KEY (disciplina_id) REFERENCES public.disciplinas(id) ON DELETE CASCADE
);

CREATE TABLE public.inscripciones (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    alumno_id BIGINT NOT NULL,
    disciplina_id BIGINT NOT NULL,
    bonificacion_id BIGINT,
    fecha_inscripcion DATE NOT NULL,
    fecha_baja DATE,
    estado VARCHAR(12) NOT NULL DEFAULT 'ACTIVA',
    costo_particular NUMERIC(19,2),
    version BIGINT NOT NULL DEFAULT 0,
    CONSTRAINT ck_inscripciones_estado CHECK (estado IN ('ACTIVA','INACTIVA','FINALIZADA')),
    CONSTRAINT ck_inscripciones_costo CHECK (costo_particular IS NULL OR costo_particular >= 0),
    CONSTRAINT ck_inscripciones_baja CHECK ((estado = 'ACTIVA' AND fecha_baja IS NULL) OR estado IN ('INACTIVA','FINALIZADA')),
    CONSTRAINT fk_inscripciones_alumno FOREIGN KEY (alumno_id) REFERENCES public.alumnos(id) ON DELETE RESTRICT,
    CONSTRAINT fk_inscripciones_disciplina FOREIGN KEY (disciplina_id) REFERENCES public.disciplinas(id) ON DELETE RESTRICT,
    CONSTRAINT fk_inscripciones_bonificacion FOREIGN KEY (bonificacion_id) REFERENCES public.bonificaciones(id) ON DELETE RESTRICT
);

CREATE UNIQUE INDEX uq_inscripciones_activas
    ON public.inscripciones (alumno_id, disciplina_id) WHERE estado = 'ACTIVA';
CREATE INDEX ix_inscripciones_alumno_estado ON public.inscripciones (alumno_id, estado);
CREATE INDEX ix_inscripciones_disciplina_estado ON public.inscripciones (disciplina_id, estado);
CREATE INDEX ix_inscripciones_bonificacion ON public.inscripciones (bonificacion_id);

CREATE TABLE public.mensualidades (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    inscripcion_id BIGINT NOT NULL,
    bonificacion_id BIGINT,
    recargo_id BIGINT,
    anio INTEGER NOT NULL,
    mes INTEGER NOT NULL,
    fecha_generacion DATE NOT NULL,
    fecha_vencimiento DATE NOT NULL,
    descripcion VARCHAR(255) NOT NULL,
    estado VARCHAR(10) NOT NULL DEFAULT 'EMITIDA',
    version BIGINT NOT NULL DEFAULT 0,
    CONSTRAINT ck_mensualidades_anio CHECK (anio BETWEEN 2000 AND 9999),
    CONSTRAINT ck_mensualidades_mes CHECK (mes BETWEEN 1 AND 12),
    CONSTRAINT ck_mensualidades_estado CHECK (estado IN ('EMITIDA','ANULADA')),
    CONSTRAINT uq_mensualidades_periodo UNIQUE (inscripcion_id, anio, mes),
    CONSTRAINT fk_mensualidades_inscripcion FOREIGN KEY (inscripcion_id) REFERENCES public.inscripciones(id) ON DELETE RESTRICT,
    CONSTRAINT fk_mensualidades_bonificacion FOREIGN KEY (bonificacion_id) REFERENCES public.bonificaciones(id) ON DELETE RESTRICT,
    CONSTRAINT fk_mensualidades_recargo FOREIGN KEY (recargo_id) REFERENCES public.recargos(id) ON DELETE RESTRICT
);

CREATE INDEX ix_mensualidades_bonificacion ON public.mensualidades (bonificacion_id);
CREATE INDEX ix_mensualidades_recargo ON public.mensualidades (recargo_id);
CREATE INDEX ix_mensualidades_vencimiento ON public.mensualidades (fecha_vencimiento, estado);

CREATE TABLE public.matriculas (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    alumno_id BIGINT NOT NULL,
    anio INTEGER NOT NULL,
    fecha_emision DATE NOT NULL,
    estado VARCHAR(10) NOT NULL DEFAULT 'EMITIDA',
